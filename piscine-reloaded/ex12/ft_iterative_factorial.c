int	ft_iterative_factorial(int nb)
{
	int	i;

	i = nb;
	if (nb == 0 || nb == 1)
		return (1);
	while (i > 1)
	{
		nb = nb * --i;
	}
	if (nb < 0)
		return (0);
	return (nb);
}
